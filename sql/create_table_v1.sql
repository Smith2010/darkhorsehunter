  sell_amount4 DECIMAL(12,0) NULL,
  sell_price5 DECIMAL(12,3) NULL,
  sell_amount5 DECIMAL(12,0) NULL,
  buy_price1 DECIMAL(12,3) NULL,
  buy_amount1 DECIMAL(12,0) NULL,
