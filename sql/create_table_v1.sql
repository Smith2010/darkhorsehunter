CREATE TABLE analysis_results (
  id BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT 'PK',
  code VARCHAR(6) NOT NULL,
  name VARCHAR(10) NULL,
  change_rate DECIMAL(12,3) NULL,
  price DECIMAL(12,3) NULL,
  amount DECIMAL(12,0) NULL,
  outstandMarketValue DECIMAL(12,2) NULL,
  today_times INT NULL,
  days INT NULL,
  datetime VARCHAR(20) NULL,
  create_datetime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  modified_datetime DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (id));