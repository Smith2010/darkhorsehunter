CREATE TABLE stock_pool (
  code VARCHAR(6) NOT NULL COMMENT '代码',
  name VARCHAR(10) NULL COMMENT '名称',
  industry VARCHAR(50) NULL COMMENT '细分行业',
  outstanding DECIMAL(12,2) NULL COMMENT '流通股本',
  totalAssets DECIMAL(12,2) NULL COMMENT '总资产(万)',
  fixedAssets DECIMAL(12,2) NULL COMMENT '固定资产',
  liquidAssets DECIMAL(12,2) NULL COMMENT '流动资产',
  totalMarketValue DECIMAL(12,2) NULL COMMENT '总市值',
  outstandMarketValue DECIMAL(12,2) NULL COMMENT '流通市值',
  realMarketValue DECIMAL(12,2) NULL COMMENT '真实流通市值',
  PRIMARY KEY (code));

